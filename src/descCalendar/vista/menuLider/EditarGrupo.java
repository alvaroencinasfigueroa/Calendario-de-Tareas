    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        Grupo g = coordinador.getLogica().getGrupos().accederM(lider);
        if (g.getLider() == g.getListIntegrantes().acceder(jListMiembros.getSelectedValue())) {
            JOptionPane.showMessageDialog(null, "No es posible eliminar al lider");
        } else {
            g.getListIntegrantes().eliminar2(jListMiembros.getSelectedValue());
            JOptionPane.showMessageDialog(null, "Eliminado con exito");
            actualizar();
        }
    }//GEN-LAST:event_jBEliminarActionPerformed

    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        Grupo g = coordinador.getLogica().getGrupos().accederM(lider);
        if (jTFNombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese el nombre del grupo");
        } else if (jCBLider.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Por favor, selecciona un lider");
        } else if (!g.getNombre().equals(jTFNombre.getText())) {
            if (coordinador.getLogica().getGrupos().existe(jTFNombre.getText())) {
                JOptionPane.showMessageDialog(null, "El nombre del grupo ya existe");
            }
        }
        Usuario u = g.getListIntegrantes().acceder(jCBLider.getSelectedItem().toString());
        u.setLider(true);
        g.setLider(u);
        lider.setLider(false);
        lider = u;
        actualizar();
        g.setNombre(jTFNombre.getText());

        JOptionPane.showMessageDialog(null, "Actualizado con éxito");
        coordinador.getLogica().escribirListas();
        coordinador.cargarPanel(4, lider);

    }//GEN-LAST:event_jBGuardarActionPerformed

