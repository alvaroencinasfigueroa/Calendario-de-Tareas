    private void jBEliminarGActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarGActionPerformed
        int res = JOptionPane.showConfirmDialog(null, "¿Seguro quiere eliminar este grupo?", "Confirmar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (res == JOptionPane.YES_OPTION) {
            Grupo g=coordinador.getLogica().getGrupos().accederM(lider);
            coordinador.getLogica().eliminarGrupo(coordinador.getLogica().getGrupos(),coordinador.getLogica().getUsuarios(), g);
            JOptionPane.showMessageDialog(null, "Eliminado con exito");
            coordinador.cargarPanel(1, lider);
        }

    }//GEN-LAST:event_jBEliminarGActionPerformed


