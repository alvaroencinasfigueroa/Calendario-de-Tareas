    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        Calendar c = Calendar.getInstance();
        if (jTFNombreTarea.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el nombre de la tarea");
        } else if (!coordinador.getLogica().validarNombreTarea(jTFNombreTarea.getText(), grupo.getNombre())) {
            JOptionPane.showMessageDialog(null, "Ingrese otro nombre para la tarea");
        } else if (jListResponsable.getSelectedValue() == null) {
            JOptionPane.showMessageDialog(null, "Por favor selecciona un responsable");
        } else if (jTADescripcion.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor escriba la descripcion de la tarea");
        } else if (jDCFecha.getDate() == null) {
            JOptionPane.showMessageDialog(null, "Por favor selecione una fecha");
        } else {
            Usuario responsable = coordinador.getLogica().getUsuarios().acceder(jListResponsable.getSelectedValue());
            Calendar fEntrega = jDCFecha.getCalendar();
            fEntrega.set(Calendar.HOUR, (int) jSHora.getValue());
            fEntrega.set(Calendar.MINUTE, (int) jSMinuto.getValue());
            if (fEntrega.compareTo(c) != 1) {
                JOptionPane.showMessageDialog(null, "Seleccione una fecha valida");
            } else {
                Tarea tarea = new Tarea(jTFNombreTarea.getText(), jTADescripcion.getText(), responsable, fEntrega);
                grupo.agregarTarea(tarea);
                JOptionPane.showMessageDialog(null, jTFNombreTarea.getText() + " Actualizada con éxito");
                coordinador.getLogica().escribirListas();
                limpiar();
            }
        }
    }//GEN-LAST:event_jBGuardarActionPerformed

