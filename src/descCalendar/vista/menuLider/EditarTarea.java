    public void llenarJListMiembros(Coordinador c, Usuario l) {
        miembros.removeAllElements();
        int i = 0;
        LIntegrantes u = c.getLogica().getGrupos().accederM(l).getListIntegrantes();
        while (u.accederP(i) != null) {
            miembros.addElement(u.accederP(i).getUserName());
            i++;
        }
    }

    public void limpiar() {
        jTFNombreTarea.setText("");
        jTADescripcion.setText("");
        jTADetallesResponsable.setText("");
        jSHora.setValue(1);
        jSMinuto.setValue(0);
        jDCFecha.setCalendar(null);
    }

    public void llenarCampos(String nTarea) {
        jTFNombreTarea.setText(nTarea);
        jTADescripcion.setText(grupo.getListTareas().accederNom(nTarea).getDescripcion());
        Date fEntrega = grupo.getListTareas().accederNom(nTarea).getfEntrega();
        jDCFecha.setDate(fEntrega);
        jSHora.setValue(fEntrega.getHours());
        jSMinuto.setValue(fEntrega.getMinutes());
    }

    public void llenarJCBTarea(Coordinador c, Usuario l) {
        jCBTarea.removeAllItems();

        LTareas u = c.getLogica().getGrupos().accederM(lider).getListTareas();
        int i = 0;
        while (u.accederP(i) != null) {
            jCBTarea.addItem(u.accederP(i).getNombre());
            i++;
        }
    }

    public void llenarJCBTarea(LTareas u) {
        jCBTarea.removeAllItems();
        int i = 0;
        while (u.accederP(i) != null) {
            jCBTarea.addItem(u.accederP(i).getNombre());
            i++;
        }
    }

    public void setLider(Usuario l) {
        lider = l;
        grupo = coordinador.getLogica().getGrupos().accederM(lider);
    }

