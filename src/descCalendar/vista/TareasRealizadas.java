    private void jBBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarActionPerformed
        if(!jTextBuscar.getText().equals("")){
            if(!jTextBuscar.getText().equals("")){
            LTareas u = coordinador.getLogica().buscarCoincidenciasTareas(grupo.getHistorial(), jTextBuscar.getText());
            llenarJCBTarea(u);
            }
        }
    }//GEN-LAST:event_jBBuscarActionPerformed

    private void jCBTareasRealizadasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCBTareasRealizadasActionPerformed
        jTTareasSelect.setText("");
        jTextComentarios.setText("");
        
        if(jCBTareasRealizadas.getSelectedItem()!=null){
            Tarea t = grupo.getHistorial().accederNom(jCBTareasRealizadas.getSelectedItem().toString());
            String fichero = t.getFichero();
            jTFNombreArchivo.setText(fichero);
            jTTareasSelect.setText(grupo.getHistorial().accederNom(jCBTareasRealizadas.getSelectedItem().toString()).toString());
            jTextComentarios.setText(grupo.getHistorial().accederNom(jCBTareasRealizadas.getSelectedItem().toString()).toStringComentarios());
        }
    }//GEN-LAST:event_jCBTareasRealizadasActionPerformed

    private void jBAbrirArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAbrirArchivoActionPerformed
        try{
            ProcessBuilder pB = new ProcessBuilder();
            pB.command("cmd.exe","/c",jTFNombreArchivo.getText());
            pB.start();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "No es posible abrir el archivo");
        }
    }//GEN-LAST:event_jBAbrirArchivoActionPerformed
    
