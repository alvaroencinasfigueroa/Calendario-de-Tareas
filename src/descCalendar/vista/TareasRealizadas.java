    public void reinicio(){
        jTextComentarios.setText("");
        jTTareasSelect.setText("");
        jTextBuscar.setText("");
    }
    
    public void llenarJCBTarea(Coordinador c, Usuario l) {
       jCBTareasRealizadas.removeAllItems();

        LTareas u = c.getLogica().getGrupos().accederM(us).getListTareas();
        int i = 0;
        while (u.accederP(i) != null) {
            jCBTareasRealizadas.addItem(u.accederP(i).getNombre());
            i++;
        }
    }
    
    public void llenarJCBTarea(LTareas u) {
        jCBTareasRealizadas.removeAllItems();
        int i = 0;
        while (u.accederP(i) != null) {
            jCBTareasRealizadas.addItem(u.accederP(i).getNombre());
            i++;
        }
    }
    
    public void actualizarListTareasRea(){
        jCBTareasRealizadas.setModel(coordinador.getLogica().getNomTareasRealizadas(us));
    }

